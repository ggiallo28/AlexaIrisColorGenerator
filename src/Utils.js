    hex_mean(hexs, color_name){
        if ( !Array.isArray(hexs) )
            return hexs;
        //LOG Del nome di questo colore
        let size = hexs.length;
        let sum_r = 0;
        let sum_g = 0;
        let sum_b = 0;
        for ( var i = 0; i < size; i++ ){
            sum_r += parseInt(hexs[i].substring(0,2), 16);
            sum_g += parseInt(hexs[i].substring(2,4), 16);
            sum_b += parseInt(hexs[i].substring(4,6), 16);
        }
        return rgb2hex((sum_r/size),(sum_g/size), (sum_b/size));
    }

    name2hex(color_name, ColorMatrix){
        // aggiungere qua nomi strani "alias"
        // se alias rimuovi duplicati prima della media
        let source = this.tokenize(color_name);
        let source_join = source.join();

        let matrix = [];
        for ( var i = 0; i < source.length; i++ ) {
            matrix[i] = [];
        }

        for ( var i = 0; i < ColorMatrix.length; i++ ){
            let target = this.tokenize(ColorMatrix[i][0]);
            if ( source_join == target.join() ){
                for ( var t = 0; t < source.length; t++ ) {
                    matrix[t] = ColorMatrix[i][1];
                }
                break;
            }
            for ( var j = 0; j < Math.min(source.length, target.length); j++){
                let match = true;
                for ( var k = j; k >= 0; k-- ){
                    match = source[k] == target[k] && match;
                    if (!match) break;
                }
                if ( match ){
                    matrix[j].push(ColorMatrix[i][1]);
                }
            }
        }

        let hex_value;
        for ( var i = source.length-1; i >=0; i-- ) {
            if ( matrix[i].length > 0 ){
                hex_value = this.hex_mean(matrix[i], color_name);
                break;
            }
        }

        if ( color_name == undefined)
            console.log('undefined'); //LOG Del nome di questo colore
        return hex_value
    }

