    tokenize(word){
        const oldWord = word.replace(/[^a-zA-Z0-9]/g, ' ').split(' ');
        const newWord = sw.removeStopwords(oldWord, sw.it);
        return sw.removeStopwords(newWord, ['','percento']);
    }

    rgb2hex(R,G,B){
        let str_r = R == 0 ? "00" : R.toString(16).toUpperCase();
        let str_g = G == 0 ? "00" : G.toString(16).toUpperCase();
        let str_b = B == 0 ? "00" : B.toString(16).toUpperCase();
        return str_r + str_g + str_b;
    }

    hex2rgb(hex){
        return {
            r: parseInt(hex.substring(1,3), 16),
            g: parseInt(hex.substring(3,5), 16),
            b: parseInt(hex.substring(5,7), 16)
        }
    }

